                    <p>Ежегодно проектируется и строится огромное количество футбольных полей с подогревом. Проектировщики и монтажники применяют множество вариантов компоновок, способов монтажа и обслуживания. При отсутствии опыта допускается множество ошибочных решений, которые приводят к ненадежности и неэффективности систем.</p>
                    <p>Занимаясь системами подогрева более 20 лет, можно сформулировать основные требования и на начальном этапе исключить ложные решения.</p>
                </div>
                <div class="heating-section__col">
                    <p>Основные требования к системе подогрева, которым полностью удовлетворяют продукты ФеСПО:</p>
                    <p>Надежность системы</p>
                    <ul>
                        <li>Качественные и проверенные материалы используемые при монтаже системы подогрева.</li>
                        <li>Простые и проверенные решения при проектировании и монтаже.</li>
                        <li>Самое важное – заложить в основание футбольного поля надежную тепловую сеть, которую в процессе эксплуатации невозможно будет поменять без вмешательства в целостность газона, основания и смежных коммуникаций. Монолитная система, состоящая из однородных материалов самое надежное решение. Не менее важен правильный расчет ИТП и теплоснабжение.</li>
                        <li>Правильно подобранное оборудование и его расчет.<br>
                            При проектировании футбольного поля необходимо учитывать источник теплоснабжения, геологические и геодезические данные, климатические условия, совмещение смежных проектируемых или существующих коммуникаций и сооружений, тип газона и его уникальные параметры, требования к эксплуатации и обслуживанию.</li>
                        <li>Правильный выбор источника тепла.<br>
                            В качестве источника тепла может быть существующая теплосеть, котельная или электрический котел. При расчете технико-экономических показателей важно рассмотреть все возможные варианты.
                        <li>Защищенность системы.<br>
                            Лучшим способом защитить трубопроводы системы подогрева футбольного поля является расположение их в слое песка. В идеале песок должен быть без примеси глины, пылеватых частиц, мусора и камней, иметь высокую водопроницаемость. Песок плотно прилегает к греющим трубам и обеспечивает идеальные фиксацию, теплосъем и распределение тепла. Никакие термомодули, решетки и экраны не заменят песок в основании футбольного поля без ущерба основным характеристикам</li>
                    </ul>
                    <p class="text-uppercase">Эффективность - равномерное распределение тепла по всей поверхности футбольного поля, автоматическое управление и окупаемость.</p>
