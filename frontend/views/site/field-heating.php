                <div class="heating-section__col">
                    <p>Для этого необходимо соблюдение нескольких правил:</p>
                    <p>Единица теплоносителя должна проходить через футбольное поле по любой из греющих труб за одно и тоже время, т.е. любая единица теплоносителя должна проходить одинаковую длину движения за один этап циркуляции. Для этого применяют петлю Тихельмана, но с некоторым отличием от применяемой схемы в отоплении зданий.
                        На схеме показано, что теплоноситель приходит из ИТП в поле и распределяется по греющим трубам через подающий коллектор, а возвращается в ИТП через обратный.</p>
                    <p>Единица теплоносителя на входе в поле попадет в первую греющую трубу через точку 1.1, пройдя через поле она попадает в обратный коллектор через т. 1.2. и чтобы попасть в теплосеть, идущую в ИТП ей придется преодолеть расстояние до противоположной стороны поля и вернуться. Таким образом данная единица теплоносителя проходит расстояние теплотрассы от ИТП до поля, 2 длины поля в греющей трубе и 2 ширины поля в обратном коллекторе.</p>
                    <p>Единица теплоносителя, попадающая в греющую трубку в точке 2.1 уже прошла ½ ширины поля и при попадании в обратный коллектор, в точке 2.2. до теплосети она проходит ½ ширины поля и 1 полную ширину.<br>
                        Единица теплоносителя, попадающая в греющую трубку в точке 3.1 уже прошла 1 ширину поля и при попадании в обратный коллектор, в точке 3.2. до теплосети она проходит еще 1 ширину поля.</p>
                    <p>Таким образом каждая единица теплоносителя проходит одинаковое расстояние за один этап циркуляции.</p>
