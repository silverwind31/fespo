                        <li>Скорость течения теплоносителя в греющих трубках в среднем 0,3-0,5 м/с, а в коллекторе и теплосети 1,5-2 м/с. Поэтому увеличение длины греющей петли  до 250 метров и более не сильно повлияет на ее сопротивление, а увеличение длины коллектора существенно.</li>
                        <li>Сбалансированность системы хуже. </li>
                        <li>Увеличит объем системы, а значит потребует более мощного насоса – затраты  на электроэнергию. </li>
                        <li>Удорожание системы из-за большего количества материалов и теплоносителя. </li>
                        <li>При длинном коллекторе примерно в 2 раза увеличивается количество стыков  при монтаже и соответственно вырастает вероятность брака в результате человеческого фактора.</li>
                    </ol>
                </div>
                <div class="heating-section__col">
                    <p>· Отсутствие воздуха в системе.</p>
                    <p>Для высокой эффективности необходимо во время пуско-наладочных работ удалить воздух из системы подогрева. Для этого систему проектируют таким образов, что бы торцы подающего и обратного коллекторов были выше теплосети футбольного поля и в них врезаются спускники воздуха. В ИТП в верхних точках трубопроводов устанавливают воздухоотводчики.</p>
                    <p>· Автоматическое управление.</p>
                    <p>Система должна быть оборудована автоматизацией, предусматривающей аварийное отключение при утечке теплоносителя в относительно небольших объемах (0,2-0,3% от объема системы).</p>
                </div>
            </div>
        </div>
        <div id="heating-section5" class="heating-section">
            <h2 class="heating-section__title">Эксплуатация и обслуживание</h2>
            <div class="heating-section__row">
                <div class="heating-section__col">
                    <p>В процессе эксплуатации исправной системы подогрева, как правило не возникает проблем, но бывают и нештатные ситуации. Практически все случаи неисправностей наших систем возникали из-за человеческого фактора. </p>
                </div>
                <div class="heating-section__col">
                    <p>Поэтому при непонятной ситуации лучше позвонить в техническую поддержку и проконсультироваться. Обычно для решения проблемы достаточно консультации. Но чтобы избежать поломок, периодически рекомендуется проводить обследование системы.</p>
                </div>
                <div class="heating-section__col">
                    <ol>
                        <p>Ежегодно необходимо проводить ревизию системы:</p>
                        <li>Испытать систему на герметичность</li>
                        <li>Проверить работоспособность КИП</li>
                        <li>Установить рекомендуемый уровень теплоносителя в подпиточном баке (в течении года утечки теплоносителя в стандартном поле могут составляет до 60-80 литров в результате инфильтрации и работ по обслуживанию)</li>
                        <li>Проверить работоспособность обратных клапанов, предохранительного клапана, регулировочного клапана и воздухоотводчиков (воздухоотводчики со временем могут залипать, что приведет к утечкам теплоносителя).</li>
                    </ol>
                </div>
                <div class="heating-section__col">
                    <p>Каждые 5 -10 лет или чаще (в зависимости от качества сетевой воды) выполнять промывку теплообменника.<br>
                        Каждые 10 лет рекомендуется менять теплоноситель<br>
                        Каждые 25 лет или при выходе из строя производить замену мембранных баков</p>
                    <p>При замене покрытия или ремонте основания футбольного поля, при ремонте водосборных лотков или бордюрного камня по периметру футбольного поля или ремонте и обслуживании смежных коммуникаций необходимо несколько раз в день следить за давлением.<br> При резком падении давления необходимо найти и устранить повреждение.</p>
                </div>
            </div>
        </div>
    </div><!-- end center-wrapper -->
